 #include <iostream>

inline int add(int num1, int num2)
{
    return num1 + num2;
}

inline int sub(int num1, int num2)
{
	return num1 - num2;
}

inline int mul(int num1, int num2)
{
	return num1 * num2;
}

inline float div(int num1, int num2)
{
	return (float)num1 / (float)num1;
}